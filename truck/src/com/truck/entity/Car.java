package com.truck.entity;

public class Car {
	private String id;
	private String truckbirth;
	private String length;
	private String width;
	private String height;
	private String weight;
	private String variety;
	private String truckcardnumber;
	public String getTruckbirth() {
		return truckbirth;
	}
	public void setTruckbirth(String truckbirth) {
		this.truckbirth = truckbirth;
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getVariety() {
		return variety;
	}
	public void setVariety(String variety) {
		this.variety = variety;
	}
	public String getTruckcardnumber() {
		return truckcardnumber;
	}
	public void setTruckcardnumber(String truckcardnumber) {
		this.truckcardnumber = truckcardnumber;
	}


}
